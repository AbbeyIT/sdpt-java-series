package java_fundamentals_activity;

import java.util.Scanner;

public class PairedArrayByIndex {

	public static void main(String[] args) {
		String email[] = {"Oscar@email.com", "Max@email.com", "Lando@email.com"};
		String username[] = {"GoodForNo.2Driver", "SimplyLovely", "WDC"};
		String password[] = {"password1", "qwerty", "1234567"};
		
		int i = 2;
		System.out.println("Index: " + i);
		System.out.println("Email: " + email[i]);
		System.out.println("Username: " + username[i]);
		System.out.println("Password: " + password[i]);
	}

}
